class ItemModel {
  final String imagePath;
  final String name;
  final String title;
  final String rate;
  final String description;
  final String timeDescription;
  final String subTitle;
  final String price;

  ItemModel({
    required this.imagePath,
    required this.name,
    required this.title,
    required this.rate,
    required this.description,
    required this.timeDescription,
    required this.subTitle,
    required this.price,
  });
}
